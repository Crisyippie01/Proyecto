    private void jbtn_editarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_editarEmpleadoActionPerformed

        // Inicializar Cargo
        int cargo = 0;

        // Capturar los datos del registro
        String rut = this.jtxt_rut.getText().trim();
        String nombre = this.jtxt_nombre.getText().trim();
        if (this.jcbx_cargo.getSelectedIndex() == 1) {
            cargo = 1;
        }
        else{
            if (this.jcbx_cargo.getSelectedIndex() == 2) {
                cargo = 2;
            }
            else{
                if (this.jcbx_cargo.getSelectedIndex() == 3) {
                    cargo = 3;
                }
                else{
                    JOptionPane.showMessageDialog(null, "Cargo invalido", "Cargo",1);
                }
            }
        }
        double sueldo = Double.parseDouble(this.jtxt_sueldo.getText().trim());

        // Crear un objeto Empleado
        Empleado empleado = new Empleado(rut, nombre, cargo, sueldo);

        // Crear un objeto RegistroEmpleado
        RegistroEmpleado registro = new RegistroEmpleado();
        // Ingreso de Cliente
        if (registro.Editar(empleado)) {
            JOptionPane.showMessageDialog(null, "Se ha editado el empleado " + nombre, "Guardando",1);
        }
        else {
            JOptionPane.showMessageDialog(null, "Hubo un error en el registro de " + nombre, "No guardado",1 );
        }
    }//GEN-LAST:event_jbtn_editarEmpleadoActionPerformed

